    private void setData(int count, float range) {

        LineDataSet set1 = new LineDataSet(mCsvDataP.getSignalTimeData("RPM"), "RPM");
        set1.setAxisDependency(AxisDependency.LEFT);
        set1.setColor(ColorTemplate.getHoloBlue());
        set1.setValueTextColor(ColorTemplate.getHoloBlue());
        set1.setLineWidth(1.5f);
        set1.setDrawCircles(false);
        set1.setDrawValues(false);
        set1.setFillAlpha(65);
        set1.setFillColor(ColorTemplate.getHoloBlue());
        set1.setHighLightColor(Color.rgb(244, 117, 117));
        set1.setDrawCircleHole(false);

        // create a data object with the data sets
        LineData data = new LineData(set1);
        data.setValueTextColor(Color.WHITE);
        data.setValueTextSize(9f);

        LineDataSet set2 = new LineDataSet(mCsvDataP.getSignalTimeData("TPS"), "TPS");
        set2.setAxisDependency(AxisDependency.LEFT);
        set2.setColor(Color.rgb(0, 250, 117));
        set1.setValueTextColor(ColorTemplate.getHoloBlue());
        set2.setLineWidth(1.5f);
        set2.setDrawCircles(false);
        set2.setDrawValues(false);
        set2.setFillAlpha(65);
        set2.setFillColor(Color.rgb(0, 250, 117));
        set2.setHighLightColor(Color.rgb(0, 250, 117));
        set2.setDrawCircleHole(false);

        data.addDataSet(set2);
        // set data
        chart.getAxisLeft().setAxisMaximum(data.getYMax()*1.2f);
        chart.setData(data);

        Legend l = chart.getLegend();
        l.setWordWrapEnabled(true);
        l.setVerticalAlignment(Legend.LegendVerticalAlignment.TOP);
        l.setHorizontalAlignment(Legend.LegendHorizontalAlignment.RIGHT);
        l.setOrientation(Legend.LegendOrientation.HORIZONTAL);
        l.setDrawInside(true);
        l.setEnabled(true);
    }

